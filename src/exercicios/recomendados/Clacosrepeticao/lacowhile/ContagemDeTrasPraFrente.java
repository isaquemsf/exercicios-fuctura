package exercicios.recomendados.Clacosrepeticao.lacowhile;

public class ContagemDeTrasPraFrente {
    public static void main(String[] args) {
        int num = 10;

        while (num > 2) {
            System.out.print(num + " ");
            num--;
        }

        System.out.println("Acabou!");
    }
}
