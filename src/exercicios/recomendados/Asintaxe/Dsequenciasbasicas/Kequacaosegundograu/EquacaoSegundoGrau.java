public class EquacaoSegundoGrau {
    public static void main(String[] args) {
        int a = 12;
        int b = 7;
        int c = -3;

        int delta = (b * b) - 4 * a * c;

        System.out.println("Δ = " + b + "² - 4 x " + a + " x " + c + " | Δ = " + delta);
    }
}
